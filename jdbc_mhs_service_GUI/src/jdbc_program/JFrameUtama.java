    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        // TODO add your handling code here:
        Integer id = ambilId(tf_noHapus.getText());

        if (id == null) {
            return;
        }

        Mahasiswa mhs = service.getById(id);

        if (mhs == null) {
            JOptionPane.showMessageDialog(this, "Data dengan ID " + id + " tidak ditemukan.");
            return;
        }

        int konfirmasi = JOptionPane.showConfirmDialog(
                this,
                "Yakin ingin menghapus data?\nID: " + mhs.getId() + "\nNama: " + mhs.getNama(),
                "Konfirmasi hapus",
                JOptionPane.YES_NO_OPTION
        );

        if (konfirmasi == JOptionPane.YES_OPTION) {
            service.delete(id);

            tf_noHapus.setText("");
            tf_namaHapus.setText("");
            tf_noEdit.setText("");
            tf_namaEdit.setText("");
            tf_nama.setText("");

            refreshData();
        }
    }//GEN-LAST:event_btn_hapusActionPerformed

    private void btn_ResetIndeksActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ResetIndeksActionPerformed
        // TODO add your handling code here:
        if (!service.isEmpty()) {
            int konfirmasi = JOptionPane.showConfirmDialog(
                    this,
                    "Reset AUTO_INCREMENT biasanya efektif jika tabel kosong.\nTetap lanjutkan?",
                    "Konfirmasi reset indeks",
                    JOptionPane.YES_NO_OPTION
            );

            if (konfirmasi != JOptionPane.YES_OPTION) {
                return;
            }
        }

        service.indexReset();
        refreshData();
    }//GEN-LAST:event_btn_ResetIndeksActionPerformed

