private void konfigurasiAwal() {
    ls_id.setModel(modelId);
    ls_nama.setModel(modelNama);

    tf_namaHapus.setEditable(false);

    ls_id.addListSelectionListener(e -> {
        if (!e.getValueIsAdjusting()) {
            pilihData(ls_id.getSelectedIndex());
        }
    });

    ls_nama.addListSelectionListener(e -> {
        if (!e.getValueIsAdjusting()) {
            pilihData(ls_nama.getSelectedIndex());
        }
    });

    addWindowListener(new WindowAdapter() {
        @Override
        public void windowClosing(WindowEvent e) {
            service.closeConnection();
        }
    });
}

private void refreshData() {
    modelId.clear();
    modelNama.clear();

    List<Mahasiswa> data = service.getAll();

    for (Mahasiswa mhs : data) {
        modelId.addElement(String.valueOf(mhs.getId()));
        modelNama.addElement(mhs.getNama());
    }
}

private void pilihData(int index) {
    if (sinkronPilihan) {
        return;
    }

    if (index < 0 || index >= modelId.getSize()) {
        return;
    }

    sinkronPilihan = true;
    ls_id.setSelectedIndex(index);
    ls_nama.setSelectedIndex(index);
    sinkronPilihan = false;

    String id = modelId.getElementAt(index);
    String nama = modelNama.getElementAt(index);

    tf_noEdit.setText(id);
    tf_namaEdit.setText(nama);

    tf_noHapus.setText(id);
    tf_namaHapus.setText(nama);
}

private int buatIdBaru() {
    int maxId = 0;

    List<Mahasiswa> data = service.getAll();

    for (Mahasiswa mhs : data) {
        if (mhs.getId() > maxId) {
            maxId = mhs.getId();
        }
    }

    return maxId + 1;
}

private Integer ambilId(String teks) {
    try {
        return Integer.parseInt(teks.trim());
    } catch (NumberFormatException nfe) {
        JOptionPane.showMessageDialog(
                this,
                "ID harus berupa angka.",
                "Input salah",
                JOptionPane.WARNING_MESSAGE
        );
        return null;
    }
}

private void isiEditDariId() {
    Integer id = ambilId(tf_noEdit.getText());

    if (id == null) {
        return;
    }

    Mahasiswa mhs = service.getById(id);

    if (mhs == null) {
        JOptionPane.showMessageDialog(this, "Data dengan ID " + id + " tidak ditemukan.");
        return;
    }

    tf_namaEdit.setText(mhs.getNama());
}

private void isiHapusDariId() {
    Integer id = ambilId(tf_noHapus.getText());

    if (id == null) {
        return;
    }

    Mahasiswa mhs = service.getById(id);

    if (mhs == null) {
        JOptionPane.showMessageDialog(this, "Data dengan ID " + id + " tidak ditemukan.");
        return;
    }

    tf_namaHapus.setText(mhs.getNama());
}

