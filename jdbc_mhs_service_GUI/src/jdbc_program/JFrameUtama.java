    private void btn_TambahkanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_TambahkanActionPerformed
        // TODO add your handling code here:
        String nama = tf_nama.getText().trim();

        if (nama.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama tidak boleh kosong.");
            tf_nama.requestFocus();
            return;
        }

        int idBaru = buatIdBaru();

        Mahasiswa mhs = new Mahasiswa(idBaru, nama);
        service.add(mhs);

        tf_nama.setText("");
        tf_noHapus.setText("");
        tf_namaHapus.setText("");
        tf_noEdit.setText("");
        tf_namaEdit.setText("");
        refreshData();
    }//GEN-LAST:event_btn_TambahkanActionPerformed

