    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        // TODO add your handling code here:
        Integer id = ambilId(tf_noEdit.getText());

        if (id == null) {
            return;
        }

        String namaBaru = tf_namaEdit.getText().trim();

        if (namaBaru.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama edit tidak boleh kosong.");
            tf_namaEdit.requestFocus();
            return;
        }

        Mahasiswa cek = service.getById(id);

       if (cek == null) {
           JOptionPane.showMessageDialog(this, "Data dengan ID " + id + " tidak ditemukan.");
           return;
        }

        Mahasiswa mhs = new Mahasiswa(id, namaBaru);
        service.update(mhs);

        tf_noEdit.setText("");
        tf_namaEdit.setText("");
        tf_noHapus.setText("");
        tf_namaHapus.setText("");
        tf_nama.setText("");

        refreshData();
    }//GEN-LAST:event_btn_editActionPerformed

